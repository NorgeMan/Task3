package org.ittr.tasks.task3;

public class GameMove {

    private int index;

    private String name;

    public GameMove(int idx, String name) {
        super();
        this.index = idx;
        this.name = name;
    }

    public int getIndex() {
        return index;
    }

    public String getName() {
        return name;
    }
}
